#include<cstdio> 
int main(){
	int n;
	while(scanf("%d",&n)!=EOF){
		switch(n){
			case 1:printf("3\n");break;
			case 2:printf("4\n");break;
			case 3:printf("5\n");break;
			case 4:printf("6\n");break;
			case 5:printf("7\n");break;
			case 6:printf("1\n");break;
			case 7:printf("2\n");break;
		}
	}
	return 0;
}
