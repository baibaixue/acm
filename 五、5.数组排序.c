#include<stdio.h>//数字排序 11月18日 白雪
int main(){
	int s[25]={1,3,4,6,8,10,26,39,55,67,81,99,108,123,168,342,455,467,500,890};//已排好序的20个数 
	scanf("%d",&s[20]);//插入的数放在数组尾。 
	int i,j; 
	for(i=0;i<20;i++){//冒泡排序排新数组 
		for(j=0;j<20-i;j++){
			if(s[j]>s[j+1]){
				int t=s[j];
				s[j]=s[j+1];
				s[j+1]=t;
			}
		}
	}
	printf("%d",s[0]);
	for(i=1;i<=20;i++){
		printf(" %d",s[i]);//输出数组 
	}
	printf("\n");
	return 0;
} 
